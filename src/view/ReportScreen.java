    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String dateIni = txtIniDate.getText();
        String dateFin = txtFinDate.getText();
        Calendar cal = Calendar.getInstance();  
        Line line = new Line();
        DateFormat inputFormat = new SimpleDateFormat("dd/MM/yyyy");
        DateFormat df = new SimpleDateFormat ("yyyy-MM-dd");
        String date1 = null;
        String date2 = null;
        try {
            Date date01 = inputFormat.parse(dateIni);
            date1 = df.format(date01);
            System.out.println(date1);
            Date date02 = inputFormat.parse(dateFin);
            date2 = df.format(date02);
            System.out.println(date2);
        } catch (ParseException ex) {
            Logger.getLogger(ReportScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ArrayList list = line.report(date1, date2);
        System.out.println(list);

        for (int i = 0; i < list.size(); i++) {
            String text = ""+list.get(i);
            jTextArea1.insert(text, jTextArea1.getCaretPosition());
            jTextArea1.append("\n");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

